package Buoi2;

public class SinhVien {
	String mssv;
	String hoTen;
	public SinhVien(String mssv, String hoTen) {
		super();
		this.mssv = mssv;
		this.hoTen = hoTen;
	}
	public String getMssv() {
		return mssv;
	}
	public void setMssv(String mssv) {
		this.mssv = mssv;
	}
	public String getHoTen() {
		return hoTen;
	}
	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}
	
}
